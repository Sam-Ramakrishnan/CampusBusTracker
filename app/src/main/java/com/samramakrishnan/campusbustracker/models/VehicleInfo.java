package com.samramakrishnan.campusbustracker.models;

public class VehicleInfo {
    private String id;
    private String label;


    // Getter Methods

    public String getId() {
        return id;
    }

    public String getLabel() {
        return label;
    }

    // Setter Methods

    public void setId(String id) {
        this.id = id;
    }

    public void setLabel(String label) {
        this.label = label;
    }
}